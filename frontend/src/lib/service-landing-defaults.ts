export const SERVICE_PAGE_SLUGS = [
  "electrical",
  "acoustics",
  "structured-cabling",
  "smart-home",
  "security",
  "architectural-lighting",
] as const;

export type ServicePageSlug = (typeof SERVICE_PAGE_SLUGS)[number];

export const SERVICE_PAGE_SLUG_TO_TYPE: Record<ServicePageSlug, ServiceType> = {
  electrical: "ELECTRICAL",
  acoustics: "ACOUSTICS",
  "structured-cabling": "STRUCTURED_CABLING",
  "smart-home": "SMART_HOME",
  security: "SECURITY",
  "architectural-lighting": "ARCHITECTURAL_LIGHTING",
};

export function isServicePageSlug(s: string): s is ServicePageSlug {
  return (SERVICE_PAGE_SLUGS as readonly string[]).includes(s);
}

